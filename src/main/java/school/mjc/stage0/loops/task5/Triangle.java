package school.mjc.stage0.loops.task5;

public class Triangle {
    public void printTriangle(int cathetusLength) {
        for(int i=1;i<=cathetusLength;i++){
            for(int o=0;o<i;o++){
                System.out.print(8);
            }
            System.out.print("\n");
        }

    }
}
